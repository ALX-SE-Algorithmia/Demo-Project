#include "cal.h"

/**
  * mul - multiply two int numbers
  * @a: first number
  * @b: second number
  * Return: product of two numbers
  */

int mul(int a, int b)
{
	return (a * b);
}

/**
  * mul_0 - multiply two float numbers
  * @a: first number
  * @b: second number
  * Return: product of two numbers
  */
float mul_0(float a, float b)
{
	return (a * b);
}

/**
  * mul_1 - multiply two double numbers
  * @a: first number
  * @b: second number
  * Return: product of two numbers
  */
double mul_1(double a, double b)
{
	return (a * b);
}
