#include "cal.h"

/**
  * sub - subtract two int numbers
  * @a: first number
  * @b: second number
  * Return: difference of two numbers
  */
int sub(int a, int b)
{
	return (a - b);
}

/**
  * sub_0 - subtract two float numbers
  * @a: first number
  * @b: second number
  * Return: difference of two numbers
  */
