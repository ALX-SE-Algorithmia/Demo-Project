/**
 * main - Entry point.
 * addition: Adds a and b
 * @a: First integer.
 * @b: Second integer.
 * Return: Addition of a and b
 */

int addition(int a, int b)
{
	return (a + b);
}


/**
 * subtract: Subtracts a and b
 * @a: First integer.
 * @b: Second integer.
 * Return: Subtraction of a and b
 */
int subtract(int a, int b)
{
    return (a - b);
}

/**
 * multiplication: multiplies a and b
 * @a: First integer.
 * @b: Second integer.
 * Return: Multiplication of a and b
 */
int multiplication(int a, int b)
{
    return (a * b);
}

/**
 * Division: divides a and b
 * @a: First integer.
 * @b: Second integer.
 * Return: division of a and b
 */
int division(int a, int b)
{
    return (a / b);
}

/**
 * modulo: output remainder of a and b
 * @a: First integer.
 * @b: Second integer.
 * Return: Modulo of a and b
 */
int modulo(int a, int b)
{
    return (a % b);
}

